# 清理旧的 node_modules 并使用 Node.js 24 重新安装

$ScriptDir = "E:\AI应用\云集智能音乐创意台\dist\scripts"
$Node24Dir = "$ScriptDir\node-v24.14.1-win-x64\node-v24.14.1-win-x64"
$NpmCmd = "$Node24Dir\npm.cmd"
$env:PATH = "$Node24Dir;$env:PATH"

Write-Host "使用 Node.js 24:"
&amp; $Node24Dir\node.exe --version
Write-Host "使用 npm:"
&amp; $NpmCmd --version
Write-Host ""

Write-Host "1. 清理旧的 node_modules..."
$AceStepUiDir = "$ScriptDir\ace-step-ui"
$ServerDir = "$AceStepUiDir\server"

if (Test-Path "$AceStepUiDir\node_modules") {
    Remove-Item -Path "$AceStepUiDir\node_modules" -Recurse -Force
    Write-Host "✓ 已清理 ace-step-ui/node_modules"
}

if (Test-Path "$ServerDir\node_modules") {
    Remove-Item -Path "$ServerDir\node_modules" -Recurse -Force
    Write-Host "✓ 已清理 server/node_modules"
}

Write-Host ""
Write-Host "2. 安装根目录依赖..."
Set-Location $AceStepUiDir
&amp; $NpmCmd install

Write-Host ""
Write-Host "3. 安装 server 目录依赖..."
Set-Location $ServerDir
&amp; $NpmCmd install

Write-Host ""
Write-Host "✅ 所有依赖安装完成！"
